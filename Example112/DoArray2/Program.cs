void PrintArray(int[,] num)
{
    for (int i = 0; i < num.GetLength(0); i++)
    {
        for (int j = 0; j < num.GetLength(1); j++)
        {
            if (num[i, j] == 0)
                System.Console.Write($" ");
            else
                System.Console.Write($"+");
        }
        System.Console.WriteLine();
    }
}

void FillArray(int row, int col)
{
    if (mas[row, col] == 0)
    {
        mas[row, col] = 1;
        FillArray(row - 1, col);
        FillArray(row, col - 1);
        FillArray(row + 1, col);
        FillArray(row, col + 1);
    }
}

